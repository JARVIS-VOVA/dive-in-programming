=begin
  Базовые операции со стеком называются push и pop, они добавляют и удаляют элементы в конец массива.

  Базовые операции с очередью — это
  shift (удаляет элемент из начала массива) и
  unshift (добавляет элемент в начало массива).

  Для добавления в конец массива можно также пользоваться оператором << (по существу синоним push)

  Методы shift и unshift модифицируют массив в начале,
  a push, pop и << — в конце.
=end
